package com.example._cs250a2;

public class Globals {
    private static int playerX;
    private static int playerY;

    public Globals() {}

    public static int getPlayerX() {
        return playerX;
    }

    public static void setPlayerX(int _playerX) {
        playerX = _playerX;
    }

    public static int getPlayerY() {
        return playerY;
    }

    public static void setPlayerY(int _playerY) {
        playerY = _playerY;
    }
}
